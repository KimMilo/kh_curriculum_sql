CREATE OR REPLACE VIEW VW_학생일반정보
AS 
SELECT STUDENT_NO AS 학번
 , STUDENT_NAME AS 학생이름
 , STUDENT_ADDRESS AS 주소
 FROM TB_STUDENT;
/* 11번 문제 */
CREATE OR REPLACE VIEW VW_지도면담
AS
SELECT STUDENT_NAME AS 학생이름
 , (SELECT DEPARTMENT_NAME 
 FROM TB_DEPARTMENT D
 WHERE D.DEPARTMENT_NO = S.DEPARTMENT_NO) AS 학과이름
 , NVL((SELECT PROFESSOR_NAME
 FROM TB_PROFESSOR P
 WHERE P.PROFESSOR_NO = S.COACH_PROFESSOR_NO), '지도교수없음') AS 지도교수이름
 FROM TB_STUDENT S
 ORDER BY 2
 WITH READ ONLY;
 /* 12번 문제 */
 CREATE OR REPLACE VIEW VW_학과별학생수
 AS
 SELECT D.DEPARTMENT_NAME
 , COUNT(*) AS STUDENT_COUNT
 FROM TB_DEPARTMENT D
 JOIN TB_STUDENT S 
 ON D.DEPARTMENT_NO = S.DEPARTMENT_NO
 GROUP BY D.DEPARTMENT_NAME;
