/* 15번 문제 */
SELECT 순위
 , 과목번호
 , 과목이름
 , 누적수강생수
 FROM (SELECT RANK() OVER(ORDER BY COUNT(*) DESC) AS 순위
 , G.CLASS_NO AS 과목번호
 , C.CLASS_NAME AS 과목이름
 , COUNT(*) AS 누적수강생수
 FROM TB_GRADE G
 JOIN TB_CLASS C
 ON G.CLASS_NO = C.CLASS_NO
 WHERE SUBSTR(TERM_NO, 1, 4) IN('2007', '2008', '2009')
 GROUP BY G.CLASS_NO, C.CLASS_NAME)
 WHERE 순위 <= 3;
